add = (digit1, digit2) => {
  return digit1 + digit2;
};

subtract = (digit1, digit2) => {
  return digit1 - digit2;
};

multiply = (digit1, digit2) => {
  return digit1 * digit2;
};

divide = (digit1, digit2) => {
  return digit1 / digit2;
};

module.exports = {
  add,
  subtract,
  multiply,
  divide
}
